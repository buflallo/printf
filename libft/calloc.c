#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*array;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	array = malloc(count * size);
	if (array)
		ft_bzero(array, count * size);
	return (array);
}
